    private void bnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddActionPerformed
        // TODO add your handling code here:
        Client client = (Client) clients.getSelectedItem();
        Plat plat =(Plat) plats.getSelectedItem();
        int qantite = Integer.parseInt(quantitetxt.getText());
        Date dateCommande =dateCommandetxt.getDate();
        cs.create(new Commande(client, plat, qantite, dateCommande));
        vider();
        loadCommande();
        
    }//GEN-LAST:event_bnAddActionPerformed

    private void deletebnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebnActionPerformed
        // TODO add your handling code here:        
        int reponce =JOptionPane.showConfirmDialog(this, "Voulez-Vous Vraiment Supprimer cette Commande ? ");
        if (reponce==0){
        cs.delete(cs.findById(id));
        loadCommande();
        vider();
        }
    }//GEN-LAST:event_deletebnActionPerformed

    private void commandesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_commandesMouseClicked
        // TODO add your handling code here:                                      
    int row = commandes.getSelectedRow();
    id = Integer.parseInt(model.getValueAt(row, 0).toString());
    String clientName = model.getValueAt(row, 1).toString();
    for (int i = 0; i < clients.getItemCount(); i++) {
        Object obj = clients.getItemAt(i);
        if (obj.toString().equals(clientName)) {
            clients.setSelectedItem(obj);
            break;
        }
    }
    String platName = model.getValueAt(row, 2).toString();
    for (int i = 0; i < plats.getItemCount(); i++) {
        Object obj = plats.getItemAt(i);
        if (obj.toString().equals(platName)) {
            plats.setSelectedItem(obj);
            break;
        }
    }
      quantitetxt.setText(model.getValueAt(row, 3).toString()); 
    try {
        java.util.Date date = new SimpleDateFormat("yyyy-MM-dd")
                .parse(model.getValueAt(row, 4).toString());
        dateCommandetxt.setDate(date);
    } catch (Exception e) {
        e.printStackTrace();
    }

    }//GEN-LAST:event_commandesMouseClicked

    private void updateBnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBnActionPerformed
        // TODO add your handling code here:
    int reponce =JOptionPane.showConfirmDialog(this, "Voulez-Vous Vraiment Modifier cette Commande ? ");
    if (reponce==0){
    cs.update(new Commande(id,
    (Client) clients.getSelectedItem(),
    (Plat) plats.getSelectedItem(),
    Integer.parseInt(quantitetxt.getText()),
    new java.sql.Date(dateCommandetxt.getDate().getTime())));
    loadCommande();
    vider();
        }
    }//GEN-LAST:event_updateBnActionPerformed

