    private void loadClient(){
    for (Client c : cf.findAll()){
    clients.addItem(c);
    
    }
    }
    private void loadPlat(){
    for (Plat p : ps.findAll()){
    plats.addItem(p);
    
    }
    }
    
    public void loadCommande(){
    model.setRowCount(0);
    for (Commande c : cs.findAll()){
    model.addRow(new Object[]{
         c.getId(),
        c.getClient(),
        c.getPlat(),
        c.getQuantite(),
        c.getDateCommande()
    });
    }
}

