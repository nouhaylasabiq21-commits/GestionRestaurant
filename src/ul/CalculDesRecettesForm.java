private String buildWhereClause(String periode) {
    switch (periode) {
        case "Aujourd'hui":
            return " WHERE DATE(c.dateCommande) = CURDATE() ";
        case "Cette semaine":
            return " WHERE YEARWEEK(c.dateCommande) = YEARWEEK(CURDATE()) ";
        case "Ce mois":
            return " WHERE MONTH(c.dateCommande) = MONTH(CURDATE()) AND YEAR(c.dateCommande) = YEAR(CURDATE()) ";
        default:
            return " ";
    }
}

    private void updateChartChiffreAffaires(String periode) {
    DefaultCategoryDataset dataset = new DefaultCategoryDataset();

    try {
        String sql = "SELECT p.nom, SUM(c.quantite * p.prix) as chiffreAffaires " +
                     "FROM COMMANDE c " +
                     "JOIN PLAT p ON c.plat_id = p.id " +
                     buildWhereClause(periode) +
                     "GROUP BY p.nom " +
                     "ORDER BY chiffreAffaires DESC";

        PreparedStatement ps = Connexion.getConnection().prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            String plat = rs.getString("nom");
            double ca = rs.getDouble("chiffreAffaires");
            dataset.addValue(ca, "Chiffre d'affaires", plat);
        }
        JFreeChart chart = ChartFactory.createBarChart(
                "Chiffre d'affaires par plat  " + periode,
                "Plats",
                "Chiffre d'affaires (DH)",
                dataset,
                PlotOrientation.VERTICAL,
                true, true, false
        );
        chart.setBackgroundPaint(Color.WHITE);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setBackgroundPaint(Color.white);
        plot.setRangeGridlinePaint(Color.RED);
        ChartPanel chartPanel = new ChartPanel(chart);
        pnlChart.removeAll();
        pnlChart.setLayout(new BorderLayout());
        pnlChart.add(chartPanel, BorderLayout.CENTER);
        pnlChart.revalidate();
        pnlChart.repaint();
    } catch (Exception e) {
        e.printStackTrace();
    }
}
