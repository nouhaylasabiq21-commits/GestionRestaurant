        private void calculerRecettes(String periode) {
    double total = 0;
    String sql = "";

    try {
        PreparedStatement con = Connexion.getConnection().prepareStatement(sql);

        switch (periode) {

            case "Aujourd'hui":
                sql = "SELECT SUM(c.quantite * p.prix) AS total "
                    + "FROM COMMANDE c "
                    + "JOIN PLAT p ON c.plat_id = p.id "
                    + "WHERE DATE(c.dateCommande) = CURDATE()";
                break;

            case "Cette semaine":
                sql = "SELECT SUM(c.quantite * p.prix) AS total "
                    + "FROM COMMANDE c "
                    + "JOIN PLAT p ON c.plat_id = p.id "
                    + "WHERE YEARWEEK(c.dateCommande) = YEARWEEK(CURDATE())";
                break;

            case "Ce mois":
                sql = "SELECT SUM(c.quantite * p.prix) AS total "
                    + "FROM COMMANDE c "
                    + "JOIN PLAT p ON c.plat_id = p.id "
                    + "WHERE MONTH(c.dateCommande) = MONTH(CURDATE()) "
                    + "AND YEAR(c.dateCommande) = YEAR(CURDATE())";
                break;

            case "Tout":
                sql = "SELECT SUM(c.quantite * p.prix) AS total "
                    + "FROM COMMANDE c "
                    + "JOIN PLAT p ON c.plat_id = p.id";
                break;
        }

        PreparedStatement ps = Connexion.getConnection().prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            total = rs.getDouble("total");
        }

        lblTotalRecettes.setText(total + " DH");

    } catch (Exception e) {
        e.printStackTrace();
    }
}
